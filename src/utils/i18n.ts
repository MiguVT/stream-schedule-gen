export function useTranslation() {
  const { settings, updateSettings } = useScheduleStore()
  
  const setLanguage = (lang: string) => {
    const validLangs = ['en', 'es', 'pt', 'fr', 'de', 'ja', 'ko', 'zh']
    if (validLangs.includes(lang)) {
      updateSettings({ language: lang as Language })
    }
  }
  
  const t = (key: TranslationKey): string => {
    return (translations[settings.language] || translations['en'])[key]
  }
  
  return { t, setLanguage }
}

